def suma(n1, n2):
    res = n1 + n2
    print('El resultado si fuera suma es: ', res)

def resta(n1, n2):
    res = n1 - n2
    print('El resultado si fuera resta es: ', res)

def multi(n1, n2):
    res = n1 * n2
    print('El resultado si fuera multiplicacion es: ', res)

def division(n1, n2):
    res = n1 / n2
    print('El resultado si fuera division es: ', res)

def elevar(n1, n2):
    res = n1 ** n2
    print('El resultado si fuera elevacion es: ', res)

def modulo(n1, n2):
    res = n1 // n2
    print('El resultado si fuera resta es: ', res)

